import pytest
from solution import Solution

def test_fizz_buzz():
    s = Solution()
    assert s.fizzBuzz(1) == ['1']
    assert s.fizzBuzz(15) == ['1', '2', 'Fizz', '4', 'Buzz', 'Fizz', '7', '8', 'Fizz', 'Buzz', '11', 'Fizz', '13', '14', 'FizzBuzz']
    assert s.fizzBuzz(0) == []
    assert s.fizzBuzz(-5) == []
    assert s.fizzBuzz(30) == ['1', '2', 'Fizz', '4', 'Buzz', 'Fizz', '7', '8', 'Fizz', 'Buzz', '11', 'Fizz', '13', '14', 'FizzBuzz', '16', '17', 'Fizz', '19', 'Buzz', 'Fizz', '22', '23', 'Fizz', 'Buzz', '26', 'Fizz', '28', '29', 'FizzBuzz']